#include <iostream> 

using namespace std; 

int main() {
    
    //* Now we want to print something from new line 
    //* There was two methods to print from new line 
    //? 1st. endl : endl stands for end of line this statement helps to end the current line and start execution from the new line.
    cout << "Aayush";
    cout << endl; 
    cout << "Vyas" << endl; 
    //* Together in same line we also used this endl statement 
    //? 2nd. '\n' : new line character that also used to end the current line and start execution from the new line 
    cout << "Kratik" << '\n'; 
    cout << "Vyas" << "\n";
}